bool ssz_gpio_is_valid(const SszGpioPinEx *gpio_pin){
	if (ssz_gpio_is_high(gpio_pin->gpio, gpio_pin->pin_mask) == gpio_pin->valid_value) {
		return true;
	}else{
		return false;
	}
}